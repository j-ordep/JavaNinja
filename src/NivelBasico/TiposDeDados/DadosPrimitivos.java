package NivelBasico.TiposDeDados;

public class DadosPrimitivos {
    public static void main(String[] args) {

        // Tipos primitivos
        int idade = 16; // valor máximo do int = 2.147.483.647
        double altura = 1.75;
        char sexo = 'M';
        boolean vivoOuMorto = true;
        Long saldoBancario = 999999L; // valor máximo do Long = 9.223.372.036.854.775.807

        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);
        System.out.println("Vivo ou Morto: " + vivoOuMorto);
        System.out.println("Saldo Bancário: " + saldoBancario);

    }
}
